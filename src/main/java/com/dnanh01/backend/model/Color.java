package com.dnanh01.backend.model;


public class Color {
  
    private String name;
    
    private int quantity;

    
    
	public Color() {
	
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getQuantity() {
		return quantity;
	}

	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

   
}
